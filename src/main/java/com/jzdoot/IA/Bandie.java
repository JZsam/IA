public class Bandie{
	private String spot, section,name;
	private char row;
	private int grade;
	private Attendance attendance;

	public String getSpot(){
		return spot;
	}
	public char getRow(){
		return row;
	}
	public String getSection(){
		return section;
	}
	public int getGrade(){
		return grade;
	}
	public String getName(){
		return name;
	}
	public Attendance getAttendance(){
		return attendance;
	}
}
